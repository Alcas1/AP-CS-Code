
import java.util.Scanner;
import java.util.*;
public class Loops
{
    public static void main (String args[])
    {
        for(int i=0;i<=36;i+=3)
        {
            System.out.println(i);
        }
        for(int i=87;i>=2;i-=5)
        {
            System.out.println(i);
        }
        for(int i=1;i<=105;i+=4)
        {
            System.out.println(i);
        }
        for(int i=15;i>=3;i--)
        {
            System.out.println(Math.pow(i,2));
        }
        
    }
}
